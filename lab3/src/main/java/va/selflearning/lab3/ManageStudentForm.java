    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        validateStudentId(sb);
        
        if(txtFullname.getText().equals("")){
        sb.append("Student name empty!\n");
        txtFullname.setBackground(Color.red);
        }
        else {txtFullname.setBackground(Color.white);}
        
        if(sb.length()> 0){
            JOptionPane.showMessageDialog(this, sb.toString(),"ERROR", JOptionPane.ERROR_MESSAGE);
            return; 
        }
          
        insertStudent(); 
        
        fillTable();
        btnAddActionPerformed(evt);
    }//GEN-LAST:event_btnAddActionPerformed

    private void insertStudent() throws NumberFormatException {
        Student std = new Student();
        std.setStudentId(txtStudentId.getText());
        std.setName(txtFullname.getText());
        std.setMajor(cbxMajor.getSelectedItem().toString());
        std.setMark(Float.parseFloat(ftxMark.getText()));
        
        list.add(std);
    }

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        StringBuilder sb = new StringBuilder();
        validateStudentId(sb);
         
        if(sb.length()> 0){
            JOptionPane.showMessageDialog(this, sb.toString(),"ERROR", JOptionPane.ERROR_MESSAGE);
            return; 
        }
        if(changeValidation())return;
        
        deleteStudent();
        fillTable();
        btnResetActionPerformed(evt);
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void deleteStudent() {
        for(Student std: list){
            if(std.getStudentId().equals(txtStudentId.getText())){
                list.remove(std);
                break;
            }
        }
    }

    private void validateStudentId(StringBuilder sb) {
        if(txtStudentId.getText().equals("")){
            sb.append("Student ID empty!\n");
            txtStudentId.setBackground(Color.red);
        }
        else {txtStudentId.setBackground(Color.white);}
    }

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
          StringBuilder sb = new StringBuilder();
        
        validateStudentId(sb);
        
        if(txtFullname.getText().equals("")){
        sb.append("Student name empty!\n");
        txtFullname.setBackground(Color.red);
        }
        else {txtFullname.setBackground(Color.white);}
        
        if(sb.length()> 0){
            JOptionPane.showMessageDialog(this, sb.toString(),"ERROR", JOptionPane.ERROR_MESSAGE);
            return; 
        }
        
        if(changeValidation())return;
        updateStudent();
        fillTable();
        btnResetActionPerformed(evt); 
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void updateStudent() throws NumberFormatException {
        for( Student std: list){
            if(std.getStudentId().equals(txtStudentId.getText())){
                std.setName(txtFullname.getText());
                std.setMajor(cbxMajor.getSelectedItem().toString());
                std.setMark(Float.parseFloat(ftxMark.getText()));
                
                break;
            }
            
        }
    }

    private boolean changeValidation() throws HeadlessException {
        int choice = JOptionPane.showConfirmDialog(this, "Do you want to make change on this student?",
                "Question",JOptionPane.YES_NO_OPTION);
        if (choice == JOptionPane.NO_OPTION) {
            return true;
        }
        return false;
    }

