    private void tblStudentsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentsMouseClicked
        int selectedRow = tblStudents.getSelectedRow(); 
        
        if(selectedRow >= 0){
            String stuId = (String) tblStudents.getValueAt(selectedRow, 0);
            
            loadStudent(stuId);
        }
    }//GEN-LAST:event_tblStudentsMouseClicked

    private void btnSortByMarkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortByMarkActionPerformed
        // TODO add your handling code here:
        Collections.sort(list, (a,b) -> (int) (a.getMark() - b.getMark()));
        
        fillTable();
    }//GEN-LAST:event_btnSortByMarkActionPerformed

    private void btnSortByNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSortByNameActionPerformed
        Collections.sort(list, new Comparator<Student>() {
            @Override
            public int compare(Student o1, Student o2) {
               return o1.getName().compareTo(o2.getName()); 
           }
        } );
        fillTable();
    }//GEN-LAST:event_btnSortByNameActionPerformed

    private void loadStudent(String stuId) {
        for(Student stu: list){
            if(stuId.equals(stu.getStudentId())){
                txtStudentId.setText(stuId);
                txtFullname.setText(stu.getName());
                ftxMark.setText("" + stu.getMark());
                cbxMajor.setSelectedItem(stu.getMajor());
                txtCapacity.setText(stu.getCapacity());
                cbxBonus.setSelected(stu.isBonnus());
                break;
            }
        }
    }

